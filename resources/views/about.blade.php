                Sphinx is an online platform where seekers of knowledge meet their destination; this is the place where professors and experts can elucidate upon their wisdom to the masses. A user can simply ask a question to the Sphinx community, and within moments, our learned community of Senior students, faculty members and alumni will try to unravel the mystery; soon the user would be flooded with a variety of solutions from people of varying levels of knowledge. The question along with all of the answers are stored for future reference, so that next time anyone has a similar query, we would be ready with all the gems they may need.
